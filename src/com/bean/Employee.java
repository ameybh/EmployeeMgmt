package com.bean;

public class Employee {

	private String  usernamei;
	private String passwordi;
	private String rolei;
	public String getUsernamei() {
		return usernamei;
	}
	public void setUsernamei(String usernamei) {
		this.usernamei = usernamei;
	}
	public String getPasswordi() {
		return passwordi;
	}
	public void setPasswordi(String passwordi) {
		this.passwordi = passwordi;
	}
	public String getRolei() {
		return rolei;
	}
	public void setRolei(String rolei) {
		this.rolei = rolei;
	}
	
	
}
